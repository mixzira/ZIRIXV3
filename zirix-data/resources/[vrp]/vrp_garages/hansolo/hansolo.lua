function src.vehicleAnchor(vehicle)
	local distance = GetDistanceBetweenCoords(GetEntityCoords(PlayerPedId()),393.26,-1618.58,29.3,true)
	if IsEntityAVehicle(vehicle) then
		if distance <= 20 then
			if vehicleanchor then
